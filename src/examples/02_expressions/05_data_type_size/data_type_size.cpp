
/*
Function int_data_size returns the memory size of an int

@param int num
@return the memory size of an int
*/
int get_int_data_type(int num)
{
    return sizeof(num);
}

int get_double_data_type(double num)
{
    return sizeof(num);
}

int get_char_size(char character)
{
    return sizeof(character);
}